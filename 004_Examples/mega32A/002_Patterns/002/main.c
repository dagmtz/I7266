/*********************************************************************************************************************************
 *          << Area for macro definitions >>
 ********************************************************************************************************************************/
#define F_CPU 1000000U
#define LED_DELAY 100U
#define START 1U
#define END 0
#define BIT_STEP 1U

/*********************************************************************************************************************************
 *          << Area for includes >>
 ********************************************************************************************************************************/
#include <stdint.h>

#include <avr/io.h>
#include <util/delay.h>


/*********************************************************************************************************************************
 *          << Main function >>
 ********************************************************************************************************************************/
int main()
{	
    uint8_t output = END;

    DDRA = 0xFF;

	while (1)
	{	
        if(output == END)
        {
            output = START;
        }
        PORTA = output;
        _delay_ms(LED_DELAY);
        output <<= BIT_STEP;
	}
	
}
