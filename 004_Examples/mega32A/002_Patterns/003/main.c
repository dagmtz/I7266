#define F_CPU 1000000U
#define LED_DELAY 50U

/*********************************************************************************************************************************
 *          << Area for includes >>
 ********************************************************************************************************************************/
#include <stdint.h>

#include <avr/io.h>
#include <util/delay.h>


/*********************************************************************************************************************************
 *          << Main function >>
 ********************************************************************************************************************************/
int main()
{	
    DDRA = 0xFF;
    PORTA = 0;

	while (1)
	{	
        PORTA += 1;
        _delay_ms(LED_DELAY);
	}
	
}
