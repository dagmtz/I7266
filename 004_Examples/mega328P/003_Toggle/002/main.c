/*********************************************************************************************************************************
 *          << Area for macro definitions >>
 ********************************************************************************************************************************/
#define F_CPU 16000000U
#define LED_DELAY 500U

/*********************************************************************************************************************************
 *          << Area for includes >>
 ********************************************************************************************************************************/
#include <avr/io.h>
#include <util/delay.h>


/*********************************************************************************************************************************
 *          << Main function >>
 ********************************************************************************************************************************/
int main()
{	
    // Set PORTD7 as output
	DDRD |= (1 << PORTD7);
    // Set PORTC5 as input
    DDRC &= ~(1 << PORTC5);
    
    // Enter an infinite loop
	while (1)
	{
        if (PINC == (1 << PINC5))
        {
            PORTD = (1 << PORTD7);
        }
        else
        {
            PORTD = 0;
        }
        
	}
}
