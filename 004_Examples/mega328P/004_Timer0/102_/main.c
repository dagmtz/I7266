#include <avr/io.h>
#include <avr/interrupt.h>

/*********************************************************************************************************************************
 *          << Main function >>
 ********************************************************************************************************************************/
int main()
{	
    TCCR1B |= (1 << CS11) | (1 << CS10);
    TIMSK1 |= (1 << TOIE1);
    sei();


    DDRD |= (1 << PORTD7);
    
    // Enter an infinite loop
	while (1)
	{

    }
}

ISR(TIMER1_OVF_vect)
{
    PORTD ^= (1 << PORTD7);
}
